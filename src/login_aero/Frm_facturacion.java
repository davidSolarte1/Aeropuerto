    public void MostrarFacturas(){
        DefaultTableModel tfacturas = new DefaultTableModel();
        tfacturas.addColumn("NÃºmero de Factura");
        tfacturas.addColumn("Nombre del Cliente");
        tfacturas.addColumn("Fecha de Factura");
        tfacturas.addColumn("Destino");
        tfacturas.addColumn("Dia de Salida");
        tfacturas.addColumn("Dia de Llegada");
        tfacturas.addColumn("Puestos Solicitados");
        tfacturas.addColumn("Valor Unitario");
        tfacturas.addColumn("Valor Total");
        tablaFacturas.setModel(tfacturas);
        
        String []datos = new String[9];
        
        try {
            Statement leer = conec.createStatement();
            ResultSet resultado = leer.executeQuery("SELECT * FROM facturas");
            
            while (resultado.next()){
                datos[0] = resultado.getString(1);
                datos[1] = resultado.getString(2);
                datos[2] = resultado.getString(3);
                datos[3] = resultado.getString(4);
                datos[4] = resultado.getString(5);
                datos[5] = resultado.getString(6);
                datos[6] = resultado.getString(7);
                datos[7] = resultado.getString(8);
                datos[8] = resultado.getString(9);

                tfacturas.addRow(datos);
            }
            tablaFacturas.setModel(tfacturas);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e + "Error en la consulta");
        }
    }

